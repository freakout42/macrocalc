.SH Limitations
.B db2rdb
can read, but not produce DBaseII-files.
.P
.B db2rdb
can read, but not produce dBMAN-V-files.
.P
.B db2rdb
does not support
.B Dbase
memo-fields.
.P
Since
.B Dbase
field names are limited to 11 characters,
the field names are shortened when transferred from
.B /rdb
to \fBDbase\fP.

